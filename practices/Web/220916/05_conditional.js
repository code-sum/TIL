/*
조건문 기본
- if, else if, else 구조
- 조건은 소괄호(condition) 안에 작성
- 실행할 코드는 중괄호{} 안에 작성
*/

if (condition) {
  // do something
} else if (condition) {
  // do something
} else {
  // do something
}

const nation = 'Korea'
if (nation === 'Korea') {
    console.log('안녕하세요!')
} else if (nation === 'France') {
    console.log('Bonjour!')
} else {
    console.log('Hello!')
}

